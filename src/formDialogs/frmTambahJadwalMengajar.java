    private void tblSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblSimpanActionPerformed
        // TODO add your handling code here:
        //validasi
        boolean isValidate = true;
        String waktuRegex = "^([0-1]?\\d|2[0-3])(?::([0-5]?\\d))?(?::([0-5]?\\d))?$";
        
        if(!txtWaktuMulai.getText().matches(waktuRegex)) {
             JOptionPane.showMessageDialog(null
                ,"Format Waktu Mulai Salah!! Format Seharusnya = HH:MM:SS Contoh = 17:50:00 ",
                "Error!",
                JOptionPane.ERROR_MESSAGE);
            isValidate = false;
        }
        
        if(!txtWaktuSelesai.getText().matches(waktuRegex)) {
             JOptionPane.showMessageDialog(null
                ,"Format Waktu Selesai Salah!! Format Seharusnya = HH:MM:SS Contoh = 17:30:00 ",
                "Error!",
                JOptionPane.ERROR_MESSAGE);
            isValidate = false;
        }

        if(txtWaktuMulai.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null
                ,"Waktu mulai harus diisi",
                "Error!",
                JOptionPane.ERROR_MESSAGE);
            isValidate = false;
        }
        
        
        
        if(txtWaktuSelesai.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null
                ,"Waktu selesai harus diisi",
                "Error!",
                JOptionPane.ERROR_MESSAGE);
            isValidate = false;
        }
        
        if(cbHari.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null
                ,"Hari harus dipilih",
                "Error!",
                JOptionPane.ERROR_MESSAGE);
            isValidate = false;
        }
        
        if(cbNip.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null
                ,"NIP harus dipilih",
                "Error!",
                JOptionPane.ERROR_MESSAGE);
            isValidate = false;
        }
        
        if(cbNoMapel.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null
                ,"No Mapel harus dipilih",
                "Error!",
                JOptionPane.ERROR_MESSAGE);
            isValidate = false;
        }
        
        String[] splitNipGuru = String.valueOf(cbNip.getSelectedItem()).split(" - ");
        String nip = splitNipGuru[0];
        System.out.println(nip);
        
        String[] splitNoMapel = String.valueOf(cbNoMapel.getSelectedItem()).split(" - ");
        String noMapel = splitNoMapel[0];
        System.out.println(nip);
        
        if (isValidate == true) {
            Database db = new Database();
            SimpleDateFormat formatTgl = new SimpleDateFormat("h:mm:ss");
            db.tambahJadwalMengajar(new JadwalMengajar(
                txtWaktuMulai.getText(),
                txtWaktuSelesai.getText(),
                cbHari.getSelectedItem().toString(),
                nip,
                noMapel
            ));
            setVisible(false);
        } else {
            isValidate = false;
            setVisible(false);
        }
        

    }//GEN-LAST:event_tblSimpanActionPerformed

